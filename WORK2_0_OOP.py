print("=== บล็อกที่ 1: ลิสต์และสไลซ์ ===")
data = [10, 20, 30, 40, 50]
a = data[1:4]      # คาดว่าได้อะไร? -> [20, 30, 40]
b = data[-2:]      # คาดว่าได้อะไร? -> [40, 50]
c = data[::2]      # คาดว่าได้อะไร? -> [10, 30, 50]
print("a =", a)
print("b =", b)
print("c =", c)

print("\n=== บล็อกที่ 2: dict & get ===")
d = {"x": 7, "y": 0}
v1 = d.get("x")         # คาดว่า -> 7
v2 = d.get("z", 999)    # คาดว่า -> 999
print("v1 =", v1)
print("v2 =", v2)

print("\n=== บล็อกที่ 3: ฟังก์ชันและ scope ===")
x = 5
def foo(n):
    x = n + 3
    return x
r = foo(4)              # คาดว่า -> 7
print("x =", x)         # คาดว่า -> 5
print("r =", r)         # คาดว่า -> 7

print("\n=== บล็อกที่ 4: เงื่อนไขซ้อน ===")
s = 72
if s >= 80:
    g = "A"
elif s >= 70:
    g = "B"
elif s >= 60:
    g = "C"
else:
    g = "F"
print("grade =", g)     # คาดว่า -> B

print("\n=== บล็อกที่ 5: วนซ้ำสะสมค่า ===")
total = 0
for i in range(1, 6):
    if i % 2 == 0:
        total += i * 2
    else:
        total += i
print("total =", total) # คาดว่า -> 35

print("\nเสร็จสิ้นงานที่ 0 — เปรียบเทียบคำตอบคาดการณ์กับผลลัพธ์จริง")
